    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //jos mitään hakutietoa ei ole annettu, pyydetään käyttäjää antamaan tieto
        LinkedList tempo = new LinkedList();
        LinkedList temporal = new LinkedList();
        boolean lopeta = false;
        if ("".equals(hakuId.getText()) && hakuAskartelu.isSelected() == false
                && hakuMyynti.isSelected() == false && "".equals(hakuNimi.getText())) {
            JOptionPane.showMessageDialog(null, "Anna jokin hakukriteeri");
        }
        //jos on annettu id haetaan sillä
        if (!"".equals(hakuId.getText())) {
            Tuote tmp = (Tuote) ajo.palautaId(Integer.parseInt(hakuId.getText()));
            if (tmp != null) {
                JOptionPane.showMessageDialog(null, tmp);
                lopeta = true;
            } else {
                JOptionPane.showMessageDialog(null, "id:llä: " + hakuId.getText() + "   ei löytynyt tuotteita");
            }
        } //jos id ei oltu annettu
        else if (lopeta == false) {
            //jos id ei ole annettu, tarkastetaan onko askartelu/myynti valittu
            if (hakuAskartelu.isSelected() == true) {
                tempo = ajo.getAskarteluLista();
            } else if (hakuMyynti.isSelected() == true) {
                tempo = ajo.getMyyntiLista();
            } else {
                tempo = ajo.getTuoteLista();
            }
            //jos tuotteelle ei ole annettu nimeä, haetaan kaikki tuotetiedot
            if ("".equals(hakuNimi.getText())) {
                tuoteListaus.setVisible(true);
                tuotelistaustila.setText("");
                for (int i = 0; i < tempo.size(); i++) {
                    String tm = tuotelistaustila.getText();
                    tuotelistaustila.setText(tm + '\n' + tempo.get(i));
                    lopeta = true;
                }
            } //jos nimi on annettu, haetaan sillä
            else if (!"".equals(hakuNimi.getText())) {
                String haku = hakuNimi.getText();
                for (int i = 0; i < tempo.size(); i++) {
                    if (ajo.palautaNimella((Tuote) tempo.get(i), haku) == true) {
                        temporal.add(tempo.get(i));

                        lopeta = true;
                    }
                }
                if (temporal.size() >= 1) {
                    tuoteListaus.setVisible(true);
                }
                for (int i = 0; i < temporal.size(); i++) {
                    String tm = tuotelistaustila.getText();
                    tuotelistaustila.setText(tm + '\n' + temporal.get(i));
                    lopeta = true;
                }
            }
        }

        if (lopeta == true) {
            tuotteenHaku.dispose();
        }

    }//GEN-LAST:event_jButton2ActionPerformed

