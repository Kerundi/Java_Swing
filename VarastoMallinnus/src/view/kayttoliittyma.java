    private void OpenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OpenActionPerformed
                        LinkedList<Tuote> tmpTuote = new LinkedList();
                LinkedList<Askartelu> tmpAsk = new LinkedList();
                LinkedList<Myynti> tmpMyynt = new LinkedList();
        Scanner lukija = new Scanner("");
        try {

            // Laitetaan oliot ensin valiaikaiseen listaan,
            // jotta voidaan varmistua etta koko operaatio joko onnistuu tai epaonnistuu
            JFileChooser jf = new JFileChooser();
            int returnVal = jf.showOpenDialog(this);
        if (returnVal == jf.APPROVE_OPTION) {
            File file = jf.getSelectedFile();            
            lukija = new Scanner(file);
        }


            // otetaan talteen siemenLuku, maxX ja maxY arvot loopin ulkopuolella tiedostosta
                       
            while (lukija.hasNextLine()) {
                String rivi = lukija.nextLine();
                String[] osat = rivi.split("[|]");

                // Poistetaan turhat valilyonnit
                for (int i = 0; i < osat.length; i++) {
                    osat[i] = osat[i].trim();
                }
                String nimi = osat[0];
                int id = Character.getNumericValue(osat[1].charAt(4));
                int hylly = Character.getNumericValue(osat[2].charAt(7));
                int paikka = Character.getNumericValue(osat[3].charAt(8));
                int Korkeus = Character.getNumericValue(osat[4].charAt(9));
                String muu = osat[5];
                        
                //jos kyseessä myytävä tuote (eli MUU voidaan muuntaa doubleksi)
                try {
                  double arvo =  Double.parseDouble(muu);

                Myynti b = new Myynti(hylly, paikka, Korkeus, id, arvo, nimi);
                tmpTuote.add(b);
                tmpMyynt.add(b);
                    
                }
                catch (Exception e)
                {
                Askartelu a = new Askartelu(hylly, paikka, Korkeus, id, muu, nimi);
                tmpTuote.add(a);
                tmpAsk.add(a);
                }
            }
            //jos tuotteita on saatu ladattu
            if (tmpAsk.size()>0 || tmpMyynt.size()>0)
            {
                            ajo.setAskarteluLista(tmpAsk);
            ajo.setMyyntiLista(tmpMyynt);
            ajo.setTuoteLista(tmpTuote);
            }

            
        } 
        catch (IOException e) {
            // Jos tiedostoa ei loydy, otetaan errori talteen ja palautetaan virheilmoitus kayttajalle
            System.out.println("Tiedostoa ei loytynyt");
        }
        finally {
            lukija.close();
        }
    }//GEN-LAST:event_OpenActionPerformed

