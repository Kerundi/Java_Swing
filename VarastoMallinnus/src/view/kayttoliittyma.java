    private void lisaaTuoteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lisaaTuoteActionPerformed
        //jos käyttäjä haluaa luoda tuotteen, on toisen valinnoista oltava valittuna
        int tmpHylly = Integer.parseInt(hyllyNumero.getText());
        int tmpPaikka = Integer.parseInt(hyllyPaikka.getText());
        int tmpKorkeus = Integer.parseInt(hyllyKorkeus.getText());
        String tmpNimi = tuotteenNimi.getText();
        String kohde = koohde.getText();
        double arvo = Double.parseDouble(hiinta.getText());
        boolean ok = false;
        int id = ajo.idpalautus();

        //jos tuote on askartelutuote
        if (Askartelu.isSelected() == true) {
            if (koohde.getText().equals("") || koohde.getText().equals("ANNA KOHDE")) {
                koohde.setText("ANNA KOHDE");
                ok = false;
            } else {
                ok = true;
            }
            //jos askartelulle on annettu kohde luodaan uusi tuote
            if (ok == true) {
                Askartelu a = new Askartelu(tmpHylly, tmpPaikka, tmpKorkeus, id, kohde, tmpNimi);
                ajo.lisaaTuote(a);
                tuotteenLisays.setVisible(false);
            }
        } //jos kyseessä myytävä tuote
        else {
            Myynti b = new Myynti(tmpHylly, tmpPaikka, tmpKorkeus, id, arvo, tmpNimi);
            ajo.lisaaTuote(b);
            tuotteenLisays.setVisible(false);
        }
        
    }//GEN-LAST:event_lisaaTuoteActionPerformed

