    private void hintaStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_hintaStateChanged
               
        if (!hinta.getValueIsAdjusting()) {
            // talletetaan uusi ja vanha
            String oldValue = hiinta.getText();
            String newValue = "" + hinta.getValue();
        
            if (oldValue.equals(newValue)) {
                return;
            }
         
            undos.addEdit(new AbstractUndoableEdit() {
                @Override
                public void undo() {
                    super.undo();
                    hiinta.setText(oldValue);
                   
                    try {
                        hinta.setValue(Integer.parseInt(oldValue));
                    } catch (NumberFormatException e) {
                    }
               
                    updateUndoElements();
                }
                
                public void redo() {
                    super.redo();
                  
                    hiinta.setText(newValue);
                    try {
                        hinta.setValue(Integer.parseInt(newValue));
                    } catch (NumberFormatException e) {
                    }
                    updateUndoElements();
                    
                }

                public String getPresentationName() {
                    return "Slider Change";
                }
            });

            hiinta.setText("" + hinta.getValue());
            updateUndoElements();
        }        // TODO add your handling code here:
    }//GEN-LAST:event_hintaStateChanged

